var count = 0;

function getRandomInt(min, max) {
    min = Math.ceil(min);
    max = Math.floor(max);
    return Math.floor(Math.random() * (max - min + 1)) + min; // حد بالا (max) نیز در نظر گرفته می‌شود
}

function btn1() {
    const btn = document.getElementById('btn1');

    btn.style.position = 'absolute';

    const rand1 = getRandomInt(1,90);
    const rand2 = getRandomInt(1,90);

    btn.style.top = `${rand1}%`;
    btn.style.left = `${rand2}%`;

    count++;

    // if (count == 5)
    //     alert('باشه دیگه بخشیدمت');
}

